template <>
void caffe_gpu_gemm<float>(const CBLAS_TRANSPOSE TransA,
    const CBLAS_TRANSPOSE TransB, const int M, const int N, const int K,
    const float alpha, const float* A, const float* B, const float beta,
    float* C) {
  // Note that cublas follows fortran order.
  int lda = (TransA == CblasNoTrans) ? K : M;
  int ldb = (TransB == CblasNoTrans) ? N : K;
  cublasOperation_t cuTransA =
      (TransA == CblasNoTrans) ? CUBLAS_OP_N : CUBLAS_OP_T;
  cublasOperation_t cuTransB =
      (TransB == CblasNoTrans) ? CUBLAS_OP_N : CUBLAS_OP_T;
	CUBLAS_CHECK(cublasSgemm(Caffe::cublas_handle(), cuTransB, cuTransA,
      N, M, K, &alpha, B, ldb, A, lda, &beta, C, N));

float* c;
float* c1;
float* c2;
float* c3;
float* c5;
float* a;
float* b;
float res[3]={31,16384,63};
float mi=4096.0;
float mw=128.0;
float mc=mw*mi;

cudaMalloc((void**)&c,M*N*sizeof(float));
cudaMalloc((void**)&c1,M*N*sizeof(float));
cudaMalloc((void**)&c2,M*N*sizeof(float));
cudaMalloc((void**)&c3,M*N*sizeof(float));
cudaMalloc((void**)&c5,M*N*sizeof(float));
cudaMalloc((void**)&a,M*K*sizeof(float));
cudaMalloc((void**)&b,K*N*sizeof(float));

cudaMemcpy(c,C,M*N*sizeof(float),cudaMemcpyDefault);
cudaMemcpy(a,A,M*K*sizeof(float),cudaMemcpyDefault);
cudaMemcpy(b,B,K*N*sizeof(float),cudaMemcpyDefault);

absolutef<<<CAFFE_GET_BLOCKS(M*K), CAFFE_CUDA_NUM_THREADS>>>(a,M*K);
absolutef<<<CAFFE_GET_BLOCKS(N*K), CAFFE_CUDA_NUM_THREADS>>>(b,N*K);
absolutef<<<CAFFE_GET_BLOCKS(M*N), CAFFE_CUDA_NUM_THREADS>>>(c,M*N);

float maxc;
float maxa;
float maxb;
float max1;
float max2;



	thrust::device_ptr<float> d_ptr = thrust::device_pointer_cast(a);
	thrust::device_ptr<float> max_ptr = thrust::max_element(d_ptr, d_ptr + M*K);
	maxa=max_ptr[0];
	d_ptr = thrust::device_pointer_cast(b);
	max_ptr = thrust::max_element(d_ptr, d_ptr + K*N);
	maxb=max_ptr[0];
	d_ptr = thrust::device_pointer_cast(c);
	max_ptr = thrust::max_element(d_ptr, d_ptr + M*N);
	maxc=max_ptr[0];
	
	max1=maxa;
	max2=maxb;

scaleqf<<<CAFFE_GET_BLOCKS(M*K), CAFFE_CUDA_NUM_THREADS>>>(A,a,mw/max1,M*K);
scaleqf<<<CAFFE_GET_BLOCKS(N*K), CAFFE_CUDA_NUM_THREADS>>>(B,b,mi/max2,N*K);
scaleqf<<<CAFFE_GET_BLOCKS(M*N), CAFFE_CUDA_NUM_THREADS>>>(C,c,mc/(max1*max2),M*N);

  CUBLAS_CHECK(cublasSgemm(Caffe::cublas_handle(), cuTransB, cuTransA,
      N, M, K, &alpha, b, ldb, a, lda, &beta, c, N));
	
remf<<<CAFFE_GET_BLOCKS(M*N), CAFFE_CUDA_NUM_THREADS>>>(c,c1,res[0],M*N);
remf<<<CAFFE_GET_BLOCKS(N*M), CAFFE_CUDA_NUM_THREADS>>>(c,c2,res[1],N*M);
remf<<<CAFFE_GET_BLOCKS(M*N), CAFFE_CUDA_NUM_THREADS>>>(c,c3,res[2],M*N);

crtf<<<CAFFE_GET_BLOCKS(M*N), CAFFE_CUDA_NUM_THREADS>>>(c1,c2,c3,c5,M*N); 
scalef<<<CAFFE_GET_BLOCKS(M*N), CAFFE_CUDA_NUM_THREADS>>>(c,C,(max1*max2)/mc,M*N);

cudaFree(a);
cudaFree(b);
cudaFree(c);
cudaFree(c1);
cudaFree(c2);
cudaFree(c3);
cudaFree(c5); 	  	
}

template <>
void caffe_gpu_gemm<double>(const CBLAS_TRANSPOSE TransA,
    const CBLAS_TRANSPOSE TransB, const int M, const int N, const int K,
    const double alpha, const double* A, const double* B, const double beta,
    double* C) {
  // Note that cublas follows fortran order.
  int lda = (TransA == CblasNoTrans) ? K : M;
  int ldb = (TransB == CblasNoTrans) ? N : K;
  cublasOperation_t cuTransA =
      (TransA == CblasNoTrans) ? CUBLAS_OP_N : CUBLAS_OP_T;
  cublasOperation_t cuTransB =
      (TransB == CblasNoTrans) ? CUBLAS_OP_N : CUBLAS_OP_T;
  CUBLAS_CHECK(cublasDgemm(Caffe::cublas_handle(), cuTransB, cuTransA,
      N, M, K, &alpha, B, ldb, A, lda, &beta, C, N));
}

