__global__
void scalef(const float *x,float *z,const float y,const int s)
{
CUDA_KERNEL_LOOP(i, s){
		z[i] = (x[i]*y);
//		printf("\nx[i]=%f\nz[i]=%f\n",x[i],z[i]);
}	
}
__global__
void qf(const float *x,float *z,const float y,const int s)
{
CUDA_KERNEL_LOOP(i, s){
		z[i] = (truncf(x[i]*y))/y;
//		printf("\nx[i]=%f\nz[i]=%f\n",x[i],z[i]);
}	
}

__global__
void scaleqf(const float *x,float *z,const float y,const int s)
{
CUDA_KERNEL_LOOP(i, s){
		z[i] = (truncf(x[i]*y));
//		printf("\nx[i]=%f\nz[i]=%f\n",x[i],z[i]);
}	
}
__global__
void remf(const float *x,float *z,const float y,const int s)
{
CUDA_KERNEL_LOOP(i, s){
	z[i] = fmodf(truncf(x[i]),y);
//	printf("\nx[i]=%f\tz[i]=%f\ty=%f\n",x[i],z[i],y);
	} 
}

__global__
void remsf(float *x,const int s)
{
CUDA_KERNEL_LOOP(i, s){
	x[i] = truncf(x[i]);
//printf("\nx[i]=%f\ty=%d\n",x[i],y);
	}
}
__global__
void absolutef(float *x,const int s)
{
CUDA_KERNEL_LOOP(i, s){
	x[i] = fabsf(x[i]);
//printf("\nx[i]=%f\ty=%d\n",x[i],y);
	}
}

